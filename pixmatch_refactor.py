def score_emoji():
    """
    Qu√© hace: Coloca un emoji dependiendo de la puntuaci√≥n del juego.
    Par√°metros: Ninguno
    Retorna: str: Un emoji correspondiente a la puntuaci√≥n actual del juego.
    """
    if mystate.myscore == 0:
        return 'üòê'
    elif -5 <= mystate.myscore <= -1:
        return 'üòè'
    elif -10 <= mystate.myscore <= -6:
        return '‚òπÔ∏è'
    elif mystate.myscore <= -11:
        return 'üòñ'
    elif 1 <= mystate.myscore <= 5:
        return 'üôÇ'
    elif 6 <= mystate.myscore <= 10:
        return 'üòä'
    elif mystate.myscore > 10:
        return 'üòÅ'


